void GameBlock::Init()
{
	Initialize(L"Asset/pic/Box.png");   //背景を初期化
	SetPos(0, -200, 0.0f);      //位置を設定
	SetSize(4000.0f, 50.0f, 0.0f);  //大きさを設定
	SetAngle(0.0f);                //角度を設定
	SetColor(1.0f, 1.0f, 1.0f, 1.0f);//角度を設定
}

